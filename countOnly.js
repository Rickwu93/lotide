// allItems: an array of strings that we need to look through
// itemsToCount: an object specifying what to count
const countOnly = function(allItems, itemsToCount) {

  const results = {}; //make an empty return

  for(const item of allItems){ //for..loop entire array
    if (itemsToCount[item]) {
      //console.log(item)
      if(results[item]){
        results[item]++ //adds item by 1
      } else {
        results[item] = 1; //counts item 
      }
    }
  }
return results;
};


